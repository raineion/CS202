// A pointer variable is a variable that stores the memory address of another variable.
// It allows direct access and manipulation of the variable it points to.

// Example:
int x = 10;       // An integer variable
int* ptr = &x;    // A pointer variable that holds the address of x

// Explanation:
// int x = 10;       // Declare an integer variable x and initialize it with the value 10
// int* ptr = &x;    // Declare a pointer variable ptr and initialize it with the address of x
//                   // The '&' operator is used to get the address of the variable x
//                   // The '*' operator is used to declare a pointer variable
